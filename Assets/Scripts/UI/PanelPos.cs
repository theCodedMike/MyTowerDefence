namespace UI
{
    public class PanelPos
    {
        public float left;
        public float bottom;
        public float right;
        public float top;


        public PanelPos(float left, float bottom, float right, float top)
        {
            this.left = left;
            this.bottom = bottom;
            this.right = right;
            this.top = top;
        }
    }
}
